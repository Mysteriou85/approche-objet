package gestionSalaire.coordonnee;

public class Adresse {

    int numeroRue;
    String nomRue;
    String codePostal;
    String ville;

    public Adresse(int numeroRue, String nomRue, String codePostal, String ville) {
        this.numeroRue = numeroRue;
        this.nomRue = nomRue;
        this.codePostal = codePostal;
        this.ville = ville;
    }

}
